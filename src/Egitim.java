public class Egitim {
    private String okul;
    private String bolum;
    private String donem;

    public Egitim(String okul, String bolum, String donem) {
        this.okul = okul;
        this.bolum = bolum;
        this.donem = donem;
    }

    public String getOkul() { return okul; }
    public String getBolum() { return bolum; }
    public String getDonem() { return donem; }
}
